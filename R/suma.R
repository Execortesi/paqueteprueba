#' Suma dos numeros
#'
#' La función `suma()´ toma dos números positivos y los suma.
#'
#' Estos son detalles extras de la función.
#'
#' @param x un vector numérico positivo
#' @param y un vector numérico positivo
#'
#' @return
#' Un vector numerico con la suma de `x´ e `y´.
#'
#' @examples
#' suma(2, 2)
#' suma(1, 10)
#'
#' @export
suma <- function(x = 0, y = 0) {

  return(x + y)
}

